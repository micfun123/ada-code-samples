def main():
    # In Python, the second argument to the range function
    # is the value where the loop stops (i.e. 6 will not be printed)
    for count in range(1, 6):
        print(count)

    
# This code will run if this file is executed directly
# (i.e. not called by another program)
if __name__ == '__main__':
    main()
